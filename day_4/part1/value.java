public class value {

    private int number;
    private boolean status;


    public value(int number, boolean status){
        number = number;
        status = status;
    }

    public void setNumber(int number){
        number = number;
    }

    public int getNumber(){
        return number;
    }

    public void setStatus(boolean bool){
        status = bool;
    }

    public boolean getStatus(){
        return status;
    }

}
